public class fluxo_de_programa_2 {
	public static void main (String [] args){
	//clausula if else
		int idade = 20;
		if(idade <=12){
			System.out.println("CRIANÇA");
		}
		else if(idade<=19){
			System.out.println("ADOLESCENTE");
		}
		else if(idade<=60){
			System.out.println("ADULTO");
		}
		else {
			System.out.println("IDOSO");
		}
	
	}
}
